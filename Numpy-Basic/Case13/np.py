# NumPy Program To Make A Array Integer From start and stop using numpy

arr = np.arange(20,101) # function is equivalent to the Python built-in range function, but returns an ndarray rather than a list.
print("Array of integers from 20 to 100")
print(arr)

# Numpy Program To make A Array of Integer From start and stop with spaced values with a specified interval

arr2 = np.arange(35,100,2)
print("Array of integers from 35 to 100 with a space values 2")
print(arr2)

